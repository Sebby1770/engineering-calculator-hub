-- Version the commercial backend in one place. Browser clients use Supabase
-- Auth only; private product data is accessed through authenticated Next.js
-- routes with the server-only service role credential.

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text,
  stripe_customer_id text,
  stripe_subscription_id text,
  subscription_status text not null default 'inactive',
  price_id text,
  current_period_end timestamptz,
  stripe_event_created_at bigint,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.donations (
  id bigint generated by default as identity primary key,
  stripe_session_id text not null,
  amount_total bigint,
  currency text,
  payment_status text,
  created_at timestamptz not null default now()
);

create table if not exists public.feedback (
  id bigint generated by default as identity primary key,
  message text not null,
  email text,
  created_at timestamptz not null default now()
);

